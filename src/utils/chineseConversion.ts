/**
 * 如果文字或字元存在於對照表中，則將其轉換為繁體中文。
 * 這是一個簡單的字典查找，而非完整的 CC 引擎。
 */
export const toTC = (text: string): string => {
    if (!text) return text;
    
    // 檢查整個字串是否在對照表中（針對術語較高效）
    if (SC_TO_TC_MAP[text]) return SC_TO_TC_MAP[text];
    
    // 否則，逐字翻譯
    return text.split('').map(char => SC_TO_TC_MAP[char] || char).join('');
};

/**
 * 將字串陣列轉換為繁體中文。
 */
export const toTCArray = (arr: string[] | undefined | null): string[] => {
    if (!arr) return [];
    return arr.map(toTC);
};
